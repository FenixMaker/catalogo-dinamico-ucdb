def tupla_para_obj(t):
    return {
        "nome": t[0],
        "autor": t[1],
        "ano": t[2],
        "paginas": t[3],
        "preco": t[4],
        "isbn": t[5],
        "descricao": t[6],
    }


def main():
    out = {}
    for genero in GENEROS:
        base = [tupla_para_obj(t) for t in ACERVO[genero]]
        extras = [tupla_para_obj(t) for t in EXTRA[genero]]
        # Evitar duplicata exata nome+autor
        seen = {(o["nome"], o["autor"]) for o in base}
        merged = list(base)
        for o in extras:
            key = (o["nome"], o["autor"])
            if key not in seen and len(merged) < 50:
                merged.append(o)
                seen.add(key)
        for t in RESERVA.get(genero, []):
            if len(merged) >= 50:
                break
            o = tupla_para_obj(t)
            key = (o["nome"], o["autor"])
            if key not in seen:
                merged.append(o)
                seen.add(key)
        if len(merged) < 50:
            raise SystemExit(f"Genero {genero!r} tem apenas {len(merged)} obras — amplie RESERVA/EXTRA.")
        out[genero] = merged[:50]
    path = Path(__file__).resolve().parents[2] / "obras-por-genero.json"
    path.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    total = sum(len(v) for v in out.values())
    print(f"Escrito {path} — {total} obras ({len(GENEROS)} gêneros × 50)")


if __name__ == "__main__":
    main()
